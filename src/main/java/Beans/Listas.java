package Beans;

public class Listas {
    private int idListas;
    private String nombre_Lista;

    public int getIdListas() {
        return idListas;
    }

    public void setIdListas(int idListas) {
        this.idListas = idListas;
    }

    public String getNombre_Lista() {
        return nombre_Lista;
    }

    public void setNombre_Lista(String nombre_Lista) {
        this.nombre_Lista = nombre_Lista;
    }
}
